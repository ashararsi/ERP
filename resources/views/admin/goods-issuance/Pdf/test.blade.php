<p><strong>Process:</strong> MIXING</p>

<p>
    Take the required quantity of sucrose syrup in the mixing vessel.<br>
    Heat the syrup up to 70°C.<br>
    Add gradually the medicinal powder and blend continuously.<br>
    <strong>Mixing Time:</strong> 15-20 mins.<br>
    Stay for 2 to 3 days so that the blend will cool down and mix properly.
</p>

